    private Detalle_Compras GenerarEntidad() {
        Detalle_Compras detalle_Compras = new Detalle_Compras();
        if (!txtId.getText().equals("")) {
            detalle_Compras.setExiste(true);
            detalle_Compras.setId(Integer.parseInt(txtId.getText()));
        }
        detalle_Compras.setId_Compra(Integer.parseInt(txtId_Compra.getText()));        
        detalle_Compras.setNombre(txtNombre.getText());  
        detalle_Compras.setCantidad(Integer.parseInt(txtCantidad.getText()));        
        detalle_Compras.setSubtotal(Float.valueOf(txtSubtotal.getText()));
        return detalle_Compras;
    }   
    
     //-----------------------------------------------------------
    DefaultTableModel modelo;
    private void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return  false;
            }
        };
        tblVendedores.setModel(modelo);
        modelo.addColumn("Codigo");
        modelo.addColumn("Id Compra");
        modelo.addColumn("Nombre");        
        modelo.addColumn("Cantidad"); 
        modelo.addColumn("Subtotal");        
    }
    
     //-------------------------------------------------------------
    private void CargarDatos(String condicion) throws Exception {
        try {
            LNDetalle_Compras logica = new LNDetalle_Compras();
            List<Detalle_Compras> lista;
            LimpiarTabla();
            Object[] fila = new Object[5];
            lista = logica.ListarRegistros(condicion);
            for (Detalle_Compras detalle_Compras : lista) {
                fila[0] = detalle_Compras.getId();
                fila[1] = detalle_Compras.getId_Compra();
                fila[2] = detalle_Compras.getNombre();
                fila[3] = detalle_Compras.getCantidad();
                fila[4] = detalle_Compras.getSubtotal();                
                modelo.addRow(fila);
            }

        } catch (Exception e) {
            throw e;
        }
    }    
    
     //-----------------------------------------------------------
