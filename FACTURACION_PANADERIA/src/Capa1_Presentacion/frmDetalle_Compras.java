    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        frmBuscarDetalle_Compras frmBuscar = new frmBuscarDetalle_Compras(null, true);
        frmBuscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {

                try {
                    int id = frmBuscar.ObtenerId();
                    String condicion = "";
                    LNDetalle_Compras logica = new LNDetalle_Compras();
                    Detalle_Compras detalle_Compras;
                    if (id > -1) {
                        condicion = String.format("ID_DETALLE=%d", id);
                        detalle_Compras = logica.ObtenerRegistro(condicion);
                        txtId.setText(String.valueOf(detalle_Compras.getId()));
                        txtId_Compra.setText(String.valueOf(detalle_Compras.getId_Compra()));
                        txtNombre.setText(detalle_Compras.getNombre());
                        txtCantidad.setText(String.valueOf(detalle_Compras.getCantidad()));
                        txtSubtotal.setText(String.valueOf(detalle_Compras.getSubtotal()));
                    } else {
                        txtId.setText("");
                        txtId_Compra.setText("");
                        txtNombre.setText("");
                        txtCantidad.setText("");
                        txtSubtotal.setText("");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        });
        frmBuscar.setVisible(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        LNDetalle_Compras logica = new LNDetalle_Compras();
        Detalle_Compras detalle_Compras;
        try {
            detalle_Compras=GenerarEntidad();
            if (detalle_Compras.isExiste()) {
                if (logica.Eliminar(detalle_Compras)>0) {
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("");

                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el registro");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar el registro que desea eliminar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el registro. "+e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblVendedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVendedoresMouseClicked

        try {
            LNDetalle_Compras logica = new LNDetalle_Compras();
            Detalle_Compras detalle_Compras;
            String condicion;
            if (evt.getClickCount() == 2) {
                int fila = tblVendedores.rowAtPoint(evt.getPoint());
                txtId.setText(tblVendedores.getValueAt(fila, 0).toString());
                condicion = String.format("ID_DETALLE=%s", txtId.getText());
                detalle_Compras = logica.ObtenerRegistro(condicion);
                txtId.setText(String.valueOf(detalle_Compras.getId()));
                txtId_Compra.setText(String.valueOf(detalle_Compras.getId_Compra()));
                txtNombre.setText(detalle_Compras.getNombre());
                txtCantidad.setText(String.valueOf(detalle_Compras.getCantidad()));
                txtSubtotal.setText(String.valueOf(detalle_Compras.getSubtotal()));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblVendedoresMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        LNDetalle_Compras logica = new LNDetalle_Compras();
        Detalle_Compras detalle_Compras = GenerarEntidad();
        try {
            if (detalle_Compras.isExiste()) {
                logica.Modificar(detalle_Compras);
            }else{
                logica.Insertar(detalle_Compras);
            }
            JOptionPane.showMessageDialog(this, logica.getMensaje());
            Limpiar();
            CargarDatos("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


