    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        LNVendedor logica = new LNVendedor();
        Vendedor vendedor;
        try {
            vendedor=GenerarEntidad();
            if (vendedor.isExiste()) {
                if (logica.Eliminar(vendedor)>0) {
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("");

                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el vendedor");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar el vendedor que desea eliminar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el vendedor. "+e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

