    private void tblVendedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVendedoresMouseClicked
        try {
            LNVendedor logica =new LNVendedor();
            Vendedor vendedor;
            String condicion;
            if (evt.getClickCount()==2) {
                int fila = tblVendedores.rowAtPoint(evt.getPoint());
                txtId.setText(tblVendedores.getValueAt(fila, 0).toString());
                condicion=String.format("ID_VENDEDOR=%s", txtId.getText());
                vendedor=logica.ObtenerRegistro(condicion);
                txtId.setText(String.valueOf(vendedor.getId()));
                txtNombre.setText(vendedor.getNombre());
                txtCedula.setText(vendedor.getCedula());
                txtCorreo.setText(vendedor.getCorreo());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblVendedoresMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        frmBuscarVendedor frmBuscar = new frmBuscarVendedor(null, true);
        frmBuscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {

                try {
                    int id = frmBuscar.ObtenerId();
                    String condicion = "";
                    LNVendedor logica = new LNVendedor();
                    Vendedor vendedor;
                    if (id > -1) {
                        condicion = String.format("ID_VENDEDOR=%d", id);
                        vendedor = logica.ObtenerRegistro(condicion);
                        txtId.setText(String.valueOf(vendedor.getId()));
                        txtNombre.setText(vendedor.getNombre());
                        txtCedula.setText(vendedor.getCedula());
                        txtCorreo.setText(vendedor.getCorreo());
                    } else {
                        txtId.setText("");
                        txtNombre.setText("");
                        txtCedula.setText("");
                        txtCorreo.setText("");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        });
        frmBuscar.setVisible(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        LNVendedor logica = new LNVendedor();
        Vendedor vendedor = GenerarEntidad();
        try {
            if (vendedor.isExiste()) {
                logica.Modificar(vendedor);
            }else{
                logica.Insertar(vendedor);
            }
            JOptionPane.showMessageDialog(this, logica.getMensaje());
            Limpiar();
            CargarDatos("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

