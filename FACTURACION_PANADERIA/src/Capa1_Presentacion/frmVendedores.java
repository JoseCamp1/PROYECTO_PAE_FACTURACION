    private Vendedor GenerarEntidad() {
        Vendedor vendedor = new Vendedor();
        if (!txtId.getText().equals("")) {
            vendedor.setExiste(true);
            vendedor.setId(Integer.parseInt(txtId.getText()));
        }
        vendedor.setNombre(txtNombre.getText());
        vendedor.setCedula(txtCedula.getText());
        vendedor.setCorreo(txtCorreo.getText());  
        return vendedor;
    }   
    //-----------------------------------------------------------
    DefaultTableModel modelo;
    private void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return  false;
            }
        };
        tblVendedores.setModel(modelo);
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Cedula");        
        modelo.addColumn("Correo"); 
    }
    //-------------------------------------------------------------
    private void CargarDatos(String condicion) throws Exception {
        try {
            LNVendedor logica = new LNVendedor();
            List<Vendedor> lista;
            LimpiarTabla();
            Object[] fila = new Object[4];
            lista = logica.ListarRegistros(condicion);
            for (Vendedor vendedor : lista) {
                fila[0] = vendedor.getId();
                fila[1] = vendedor.getNombre();
                fila[2] = vendedor.getCedula();                
                fila[3] = vendedor.getCorreo();
                modelo.addRow(fila);
            }

        } catch (Exception e) {
            throw e;
        }
    }
    //-----------------------------------------------------------
    private void Limpiar(){
        txtId.setText("");
        txtNombre.setText("");
        txtCedula.setText("");     
        txtCorreo.setText("");
    }
