    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        LNVentas logica = new LNVentas();
        Ventas ventas;
        try {
            ventas=GenerarEntidad();
            if (ventas.isExiste()) {
                if (logica.Eliminar(ventas)>0) {
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("");

                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar la venta");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar la venta que desea eliminar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se puede eliminar la venta. "+e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

