    DefaultTableModel modelo;
    private void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row,int column){
                return  false;
            }
        };
        tblVendedores.setModel(modelo);
        modelo.addColumn("Codigo");
        modelo.addColumn("Metodo Pago");
        modelo.addColumn("Fecha");        
        modelo.addColumn("Cod Cliente"); 
        modelo.addColumn("Cod Vendedor"); 
        modelo.addColumn("Total"); 
    }
    
    //-------------------------------------------------------------
    private void CargarDatos(String condicion) throws Exception {
        try {
            LNVentas logica = new LNVentas();
            List<Ventas> lista;
            LimpiarTabla();
            Object[] fila = new Object[6];
            lista = logica.ListarRegistros(condicion);
            for (Ventas ventas : lista) {
                fila[0] = ventas.getId();
                fila[1] = ventas.getMetodoPago();
                fila[2] = ventas.getFecha();
                fila[3] = ventas.getId_Cliente();
                fila[4] = ventas.getId_Vendedor();
                 fila[5] = ventas.getTotal();
                modelo.addRow(fila);
            }

        } catch (Exception e) {
            throw e;
        }
    }    
